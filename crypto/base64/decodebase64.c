int32 base64_decode(byte* src, int32 length, byte* dst) {

    int32 i, j, l;
    byte val;

    i = 0;
    l = 0;
    j = 0;

    while (i++ < length) {
        if (*src == '=') {
            return l - 1;
        }

        val = table[*src++];
        if (val == 0xff) {
            continue;
        }

        switch (j++ % 4) {
            case 0:
                *dst = val << 2;
                l++;
                break;
            case 1:
                *dst++ |= (val >> 4) & 0x3;
                *dst = val << 4;
                l++;
                break;
            case 2:
                *dst++ |= (val >> 2) & 0x3F;
                *dst = (val & 0x3) << 6;
                l++;
                break;
            case 3:
                *dst++ |= val & 0x3F;
                break;
        }
    }

    return l;
}
